List<Map<String, dynamic>> _grid({
  required String id,
  required double x,
  required double y,
  required double width,
  required double height,
  int columns = 8,
  int rows = 7,
  String color = '#33C8C0B5',
  int z = 3,
}) {
  final out = <Map<String, dynamic>>[];
  for (var i = 0; i <= columns; i++) {
    out.add(
      _box(
        id: '${id}_v$i',
        x: x + (width / columns) * i,
        y: y,
        width: 0.0012,
        height: height,
        fill: color,
        z: z,
      ),
    );
  }
  for (var i = 0; i <= rows; i++) {
    out.add(
      _box(
        id: '${id}_h$i',
        x: x,
        y: y + (height / rows) * i,
        width: width,
        height: 0.001,
        fill: color,
        z: z,
      ),
    );
  }
  return out;
}

List<Map<String, dynamic>> _dotGrid({
  required String id,
  required double x,
  required double y,
  int columns = 6,
  int rows = 6,
  double gapX = 0.026,
  double gapY = 0.019,
  String color = '#8899A1AA',
  int z = 18,
}) {
  final out = <Map<String, dynamic>>[];
  for (var row = 0; row < rows; row++) {
    for (var col = 0; col < columns; col++) {
      out.add(
        _box(
          id: '${id}_${row}_$col',
          x: x + col * gapX,
          y: y + row * gapY,
          width: 0.006,
          height: 0.0045,
          fill: color,
          radius: 999,
          z: z,
        ),
      );
    }
  }
  return out;
}

