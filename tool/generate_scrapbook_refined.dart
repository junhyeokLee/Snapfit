List<Map<String, dynamic>> _polaroid({
  required String id,
  required double x,
  required double y,
  required double width,
  required double height,
  required String imageUrl,
  double rotation = 0,
  int z = 8,
  bool label = false,
}) {
  final padX = math.min(0.026, width * 0.11);
  final padY = math.min(0.02, height * 0.09);
  final bottom = math.min(0.055, height * 0.20);
  return [
    _box(
      id: '${id}_card',
      x: x,
      y: y,
      width: width,
      height: height,
      fill: '#FFFFFFFF',
      radius: 0.004,
      rotation: rotation,
      z: z,
    ),
    _image(
      id: id,
      x: x + padX,
      y: y + padY,
      width: math.max(0.02, width - padX * 2),
      height: math.max(0.02, height - padY - bottom),
      imageUrl: imageUrl,
      rotation: rotation,
      z: z + 4,
    ),
    if (label)
      _hand(
        id: '${id}_label',
        x: x + padX,
        y: y + height - bottom + 0.012,
        width: width - padX * 2,
        height: 0.022,
        text: 'small memory',
        fontSize: 11,
        rotation: rotation,
        color: '#FF7B7168',
        z: z + 6,
      ),
  ];
}

Map<String, dynamic> _page({
  required int number,
  required String layoutId,
  required String role,
  required int photos,
  required List<Map<String, dynamic>> layers,
}) => {
  'pageNumber': number,
  'layoutId': layoutId,
  'role': role == 'cover'
      ? 'cover'
      : role == 'ending'
      ? 'end'
      : role == 'text'
      ? 'chapter'
      : 'inner',
  'recommendedPhotoCount': photos,
  'layers': _normalizeLayerOrder(layers),
};

List<Map<String, dynamic>> _normalizeLayerOrder(
  List<Map<String, dynamic>> layers,
) {
  for (var i = 0; i < layers.length; i++) {
    layers[i]['zIndex'] = i + 1;
  }
  return layers;
}

